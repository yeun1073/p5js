function setup() {
  // 캔버스 크기 600 * 400
  createCanvas(600, 400);
}

function draw() {
  // 1. 추상적인 배경 구성
  background(40, 50, 70); 
  
  noStroke();
  fill(200, 100, 150);
  triangle(0, 0, 300, 0, 0, 400); // 좌측 상단 삼각형
  
  fill(100, 200, 200);
  rect(450, 250, 150, 150); // 우측 하단 사각형
  
  stroke(200);
  line(0, 400, 600, 0); // 배경을 가로지르는 사선

  // 2. 피자를 올려둘 접시
  noStroke();
  fill(230);
  ellipse(300, 200, 380, 380);
  fill(210);
  ellipse(300, 200, 350, 350);

  // 3. 7/8 조각 피자 (중심점: 295, 195)
  stroke(180, 100, 30);
  strokeWeight(5);
  fill(230, 160, 60); // 도우 색상
  // 45도(QUARTER_PI)부터 360도(TWO_PI)까지 그림
  arc(295, 195, 320, 320, QUARTER_PI, TWO_PI, PIE);

  noStroke();
  fill(255, 210, 50); // 치즈 색상
  arc(295, 195, 290, 290, QUARTER_PI, TWO_PI, PIE);

  // 7/8 조각 토핑 - 페퍼로니 (원형)
  fill(210, 50, 40);
  ellipse(250, 150, 45, 45);
  ellipse(200, 230, 40, 40);
  ellipse(300, 100, 35, 35);
  ellipse(180, 140, 30, 30);
  ellipse(360, 160, 40, 40);
  ellipse(260, 280, 45, 45);
  ellipse(330, 260, 35, 35);
  
  // 7/8 조각 토핑 - 피망 (사각형) & 올리브 (작은 원)
  fill(40, 120, 50);
  quad(220, 120, 230, 110, 240, 130, 230, 140);
  quad(320, 130, 340, 120, 330, 150, 310, 140);
  quad(240, 240, 260, 230, 250, 260, 230, 250);
  
  fill(30);
  ellipse(280, 180, 15, 15);
  ellipse(310, 220, 15, 15);
  ellipse(180, 200, 15, 15);
  
  // 4. 1/8 조각 피자 (중심점: 315, 215)
  stroke(180, 100, 30);
  fill(230, 160, 60); // 도우
  // 0도부터 45도(QUARTER_PI)까지 그림
  arc(315, 215, 320, 320, 0, QUARTER_PI, PIE);

  noStroke();
  fill(255, 210, 50); // 치즈
  arc(315, 215, 290, 290, 0, QUARTER_PI, PIE);

  // 1/8 조각 토핑
  fill(210, 50, 40); // 페퍼로니
  ellipse(420, 250, 40, 40);
  ellipse(370, 275, 30, 30);
  
  fill(40, 120, 50); // 피망
  quad(380, 230, 395, 220, 390, 245, 375, 240);
  
  fill(30); // 올리브
  ellipse(400, 280, 15, 15);
}